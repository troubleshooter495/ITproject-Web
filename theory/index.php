			<div class="title">Аккорды</div>
			<p><b>Аккордом</b> называется одновременное сочетание трех или более звуков, которые расположены по терциям или могут быть расположены по терциям.</p>
			<p>Аккорд строится от нижнего звука вверх. От того, какие терции входят в образование трезвучия и каков порядок их расположения, зависит вид трезвучия. Из больших и малых терций образуются четыре вида трезвучий:</p>
			<p><b>Мажорное</b> или большое трезвучие состоит из б3 + м3, между крайними звуками образуется ч5</p>
			<p><b>Минорное</b> или малое трезвучие состоит из м3 + б3, между крайними звуками образуется ч5</p>
			<p><b>Увеличенное</b> трезвучие состоит из б3 + б3, между крайними звуками образуется ув5 (м6)</p>
			<p><b>Уменьшенное</b> трезвучие состоит из м3 + м3, между крайними звуками образуется ум5</p>
			<div class="ex"><img src="img/28.jpg" alt=""></div>
			<p>Трезвучие имеет два обращения: первое обращение - <b>секстаккорд</b>, оно получается от переноса примы на октаву вверх, второе - <b>квартсекстаккорд</b>, оно получается от переноса примы и терции на октаву вверх.
			Секстаккорд обозначется цифрой 6, так как для этого аккорда характерна секста, образованная от нижнего звука к перенесенной приме. Квартсекстаккорд обозначается цифрами (6/4) по интервалас образовавшимися от нижнего звука.</p>
			<div class="ex"><img src="img/29.jpg" alt=""></div>
			<p>Мажорные и минорные секстаккорд и квартсексаккорд отличаются тем что, образованны обращением мажорного и минорного тонических трезвучий соответственно.</p>
			<p>Аккорд, состоящий из четырех звуков, расположенных по терциям, называется <b>септаккордом</b>. Крайние звуки образуют септиму, от этого и происходит название аккорда</p>
