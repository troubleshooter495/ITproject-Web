			<div class="title">Ритм и метр</div>
			<p><b>Ритмом</b> называется соотношение длительностей звуков в их последовательности</p>
			<p>В музыке звуки организованы во времени. Чередование звуков равными по времени долями образует в музыке равномерное движение. В этом движении звуки некоторых долей называются <b>акцентами</b>. </p>
			<p>Доли, на которые приходятся акценты называются <b>сильными долями</b>.</p>
			<p>Равномерное чередование сильных и слабых долей называется <b>метром</b></p>
			<p>Доля метра может быть выражена различной длительностью. Выражение долей метра определенной длительстью называется <b>размером</b>.</p>
			<p>В нотном письме размеры обозначаются двумя цифрами, расположенными после знаков альтерации одна под другой. Например:</p>
			<div class="ex"><img src="img/13.jpg" alt=""></div>
			<p>Верхняя цифра обозначает количество метрических долей, а нижняя - какой длительностью выражена доля метра в данном размере. </p>
			<p>Отрезок произведения от одной сильной доли до следующей называется <b>тактом</b>.</p>
			<p>В нотной записи такты отделены друг от друга вертикальной чертой перпендикулярно нотному стану. Эта черта называется <b>тактовой чертой</b>. Тактовая черта ставится перед сильной долей, чтобы ее выделить.</p>
			<p>Размер <i>"2/4"</i> читают как <i>"размер две четверти"</i>, что означает, что в каждом такте сумарная длительность нот равна двум четвертным.</p>
			<p>Если музыка начинается со слабой доли, то вначале образуется неполный такт, который называется <b>затактом</b>. В большинстве случаев затакт не превышает половины такта. В большинстве случаев, также, произведение, начавшееся с затакта, заканчивается знеполным тактом, дополняющим затакт</p>
			<p>В конце, а иногда и по окончании части произведения ставится <b>двойная тактовая черта</b>.</p>
			<p>Чтобы не сбиться, вы можете дирижировать размер произведения. Под дирижированием подразумевается средство: во-первых, счета, то есть указания времени продолжительности и смены долей такта; во-вторых, установление темпа для данного произведения. В основу дирижирования положены двухдольные, трехдольные и четырехдольные фигуры взмахов.</p>
