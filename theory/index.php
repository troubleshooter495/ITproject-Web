			<div class="title">Лад и тональность</div>
			<p>Слушая или исполняя музыкальное произведение, мы наблюдаем, что образующие его щвуки находятся между собой в определенном соотношении. Это выражается прежде всего в том, что в процессе развития музыки, в частности мелодии, некоторые звуки, выделяясь из общей массы, приобретают характер <i>опорных</i> звуков. Мелодия обычно и заканчивается на одном из этих звуков.</p>
			<p>Опорные звуки принято называть <b>устойчивыми</b> звуками. Такое определение опорных звуков соответствует их характеру, так как окончание мелодии на опорном звуке производит впечатление устойчивости, покоя.</p>
			<p>Один из устойчивых звуков обычно выделяется больше, чем другие. Он является как бы главной опорой. Такой устойчивый звук называется <b>тоникой</b></p>
			<p>В противоположность устойчивым, другие звуки называются <b>неустойчивыми</b>.</p>
			<p>Переход неустойчивого звука в  устойчивый называется <b>разрешением</b>.</p>
			<p>Система взаимоотношения между устойчивыми и неустойчивыми звуками называется <b>ладом</b>.</p>
			<p><b>Мажорным</b> называется лад, устойчивые звуки которого образуют <b>большое или мажорное трезвучие</b> - аккорд, состоящий из трех звуков. Звуки мажорного трезвучия расположены по терциям: большая терция + малая терция. </p>
			<p>Мажорное трезвучие, построенное на тонике, называется <b>тоническим трезвучием</b></p>
			<p>Расположение звуков лада в порядке высоты (от тоники до тоники следующей октавы) называется звукорядом лада или <b>гаммой</b>.</p>
			<p>Звуки, образующие гамму называются ступенями</p>
			<p>В гамме мажорного лада семь ступеней. Ступени обозначают римскими цифрами. Ступени мажорной гаммы образуют следующую последовательность секунд:</p>
			<div class="ex">б2, б2, м2, б2, б2, б2 ,м2</div>
			<div class="ex"><img src="img/20.jpg" alt=""></div>
			<p>Натуральный мажорный лад может быть построен от любой ступени музыкального звукоряда. Высота, на которой расположен лад, называется тональностью. Название тональности происходит от названия звука принятого за тонику. Название тональности составляется из обозначения тоники и лада(в данном случае мажор). Например:</p>
			<div class="ex"> <i>До мажор</i> или <i>C-dur</i>; <br>
				<i>Соль мажор</i> или <i>G-dur</i> и т. д.
			</div>
			<p>Тональность До мажор, рассмотреная выше содержит в себе все основные ступени, однако тональности, образованные от других ступеней, содержат в себе производные ступени. Некоторые содержат только пониженные ступени - <b>бемольные тональности</b>, некоторые - только повышенные - <b>диезные тональности</b> </p>
			<p>Тональности, отличающиеся друг от друга одним ключевым знаком, являются родственными, ввиду того, что в их составе шесть общих звуков.</p>
			<p>Порядок расположения диезных тональностей идет по чистым квинтам вверх. С каждой квинтой добавляется диез. Диез ставится на ступени тоники предыдущей тональности: </p>
			<div class="ex"><img src="img/21.jpg" alt=""></div>
			<p>Порядок расположения бемольных мажорных тональностей на основе родства возникает таким же путем, но тольок вниз по чистым квинтам.</p>
			<div class="ex"><img src="img/22.jpg" alt=""></div>
			<p>Ключевые знаки энгармонически равных тональностей в сумме составляют 12 знаков. Порядок расположения мажорных тональностей: диезных вверх по чистым квинтам, а бемольных вниз - называют <b>квинтовым кругом</b></p>
			<div class="ex"><img src="img/23.jpg" alt=""></div>
			<p><b>Минорным</b> называется лад, устойчивые звуки которого образуют <b>малое или минорное трезвучие</b> - аккорд образованный малой терцие + большой терцией.</p>
			<p>Порядок последовательности секунд в гамме натурального минора следующий:</p>
