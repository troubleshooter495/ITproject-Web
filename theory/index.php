			<div class="title">Интервалы</div>
			<p>Одновременное или последовательное сочетание двух звуков называтся <b>интервалом</b></p>
			<p>Звуки интервала, взятые последовательно, образуют мелодический интервал. Звуки интервала, взятые одновременно, образуют гармонический интервал.</p>
			<p>Нижний звук интервала называется основанием интервала, а верхний - вершиной интервала.</p>
			<p>Название интервала обуславливается количеством ступеней и нот, которые он охватывает. Между основными ступенями звукоряда (в пределах октавы) образуются следующие интервалы:</p>
			<div class="ex"><img src="img/17.jpg" alt=""></div>
			<p>Например:</p>
			<div class="ex"><img src="img/18.jpg" alt=""></div>
			<p><i>Интервалы являются основой мелодии. От сочетания мелодических интервалов  в различной последовательности мелодическое движение приобретает многобразную выразительность</i></p>
			<p>Перемещение звуков интервала, благодаря которому нижний звук становится верхним, а верхний звук - нижним, называется <b>обращением интервала</b></p>
			<p>Возможны два способа перемещения звуков, а именно:</p>
			<p>1. Перенесение основания интервала (нижнего звука) на октаву вверх.</p>
			<p>2. Перенесение вершины интервала (верхнего звука) на октаву вниз.</p>
			<p>В результате этого получается новый интервал. Как правильно чистые интервалы обращаются в чистые, малые - в большие, большие - в малые.</p>
