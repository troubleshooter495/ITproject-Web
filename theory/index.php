			<div class="title">Введение в основы</div>
			<p>Музыкальная система, положенная в основу современной музыкальной практики, представляет собой ряд звуков, находящихся между собой в опеределенный высотных взаимоотношениях.Расположение звуков по высоте называется <b>звукорядом</b>, а каждый звук - его <b>ступенью.</b> Основным ступеням звукоряда музыкальной системы присвоено семь самостоятельных названий: </p>
				<div class="ex"> До, Ре, Ми, Фа, Соль, Ля, Си <br>
				C, D, E, F, G, A, B <br>
				</div>
			<p>Каждый восьмой звук (считая вверрх по белым клавишам) повторяет себя с удвоенным количеством колебаний, поэтому ступени звукоряда повторяются. Расстояние между звуками одинаковых ступеней называется <b> октавой </b>. Октавой также называется часть звукоряда, в кторую входит 7 основных ступеней. Началом октавы принято считать звук ступени До. весь звукоряд состоит из семи полных октав и четырех звуков, образующих неполные октавы.</p>
			<div class="ex"> <img src="img/1.jpg" alt=""></div>
			<p>Каждая октава делится на 12 полутонов.Полутон является самым узким расстоянием между звуками музыкальной системы. Расстояние, образованное двумя полутонами, называется <b>целым тоном</b>. Между основными ступенями звукоряда имеются два полутона и 5 целых тонов:</p>
			<div class="ex"><img src="img/2.jpg" alt=""></div>
			<p>Каждая основная ступень звукоряда может быть повышена или понижена. Звуки, с повышенными или пониженными ступенями, считаются <b> производными</b>. Повышение ступеней на полтона обозначается знаком <b> диез(#)</b>, а понижение - <b>бемоль(♭)</b>. Также существует повышение на два полутона - дубль диез(×) и понижение на два полутона - дубль бемоль(♭♭). Для отмены повышения или понижения существует <b>бекар(♮)</b></p>
		<p>Описанное повышение и понижение основных ступеней называется <b>альтерацией</b>. </p>
			<p>Для обозначения производных ступеней к буквам прибавляются слоги: is - диез, isis - дубль-диез, es - бемоль, eses - дубль-бемоль. Например:</p>
			<div class="ex">cis - до-диез, fisis - фа-дубль-диез, <br>
				des - ре-бемоль, geses - соль-дубль-бемоль.
			</div>
			<p>Исключение составляет производная ступень си-бемоль, за которой сохраняется обозначение буквой <i>B</i>, <i>b</i>.
			</p>
			<p>При гласных <i>a</i> и <i>e</i> в слоге <i>es</i> буква <i>e</i>, для удобства отбрасывается; получается:</p>
			<div class="ex">ми-бемоль не <i>ees</i>, a <i>es</i>; <br>
				ля-бемоль не <i>aes</i>, a <i>as</i>.
			</div>
